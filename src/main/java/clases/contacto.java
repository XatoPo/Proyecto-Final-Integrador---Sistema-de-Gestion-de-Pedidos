package clases;

public class contacto {
    private String id_contac;
    private String tipo_contac;
    private String telef_contac;
    private String email_contac;

    public String getEmail_contac() {
        return email_contac;
    }

    public void setEmail_contac(String email_contac) {
        this.email_contac = email_contac;
    }

    public String getId_contac() {
        return id_contac;
    }

    public void setId_contac(String id_contac) {
        this.id_contac = id_contac;
    }

    public String getTipo_contac() {
        return tipo_contac;
    }

    public void setTipo_contac(String tipo_contac) {
        this.tipo_contac = tipo_contac;
    }

    public String getTelef_contac() {
        return telef_contac;
    }

    public void setTelef_contac(String telef_contac) {
        this.telef_contac = telef_contac;
    }
    
}
