package clases;

public class ubigeo {
    private String id_ubigeo;
    private String distrito_ubi;
    private String provincia_ubi;
    private String calle_avend_ubi;
    private int num_calle_ubi;
    private String referencia_ubi;
    private String id_emp;

    public void setId_emp(String id_emp) {
        this.id_emp = id_emp;
    }
    
    public String getReferencia_ubi() {
        return referencia_ubi;
    }

    public void setReferencia_ubi(String referencia_ubi) {
        this.referencia_ubi = referencia_ubi;
    }

    public String getId_ubigeo() {
        return id_ubigeo;
    }

    public void setId_ubigeo(String id_ubigeo) {
        this.id_ubigeo = id_ubigeo;
    }

    public String getDistrito_ubi() {
        return distrito_ubi;
    }

    public void setDistrito_ubi(String distrito_ubi) {
        this.distrito_ubi = distrito_ubi;
    }

    public String getProvincia_ubi() {
        return provincia_ubi;
    }

    public void setProvincia_ubi(String provincia_ubi) {
        this.provincia_ubi = provincia_ubi;
    }

    public String getCalle_avend_ubi() {
        return calle_avend_ubi;
    }

    public void setCalle_avend_ubi(String calle_avend_ubi) {
        this.calle_avend_ubi = calle_avend_ubi;
    }

    public int getNum_calle_ubi() {
        return num_calle_ubi;
    }

    public void setNum_calle_ubi(int num_calle_ubi) {
        this.num_calle_ubi = num_calle_ubi;
    }
    
}
